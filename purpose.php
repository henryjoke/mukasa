					<h5>Our Purpose</h5>
					<p align= "justify">All traditional Catholics will agree that the need for well-trained and dedicated priests is urgent. The harvest is certainly great, but the laborers are very few indeed. How can we best remedy this situation? This is a question that greatly concerns all faithful Catholics.
<p align="justify">
In my years of education I have been concerned with the many boys who show promising signs of a priestly vocation during their early teenage years, only to lose interest as they approach the final 2 or 3 years of high school. Why is that? Because even the best-trained boys are strongly influenced by our three-fold enemy the world, the flesh and the devil.
<p align="justify">
This problem is not peculiar to our age. In Session XXIII, the Council of Trent declared that young aspirants to the priesthood should be formed in habits of piety and discipline from their earliest years. Consequently, in our country prior to Vatican II, as elsewhere in the Catholic world, preparatory seminaries were common. The Church understands that boys who are called by God need an environment in which they can preserve their vocation during the turbulent years of adolescence.
<p align="justify">
A vocation to the priesthood is often compared to a seed. When a seed germinates the small plant needs to be watered, sheltered and nourished during the early stages of growth. So too, a vocation is a delicate seed that must be nourished and protected. Not only must a vocation be protected from the foul breath of the world, but good habits must be formed in the young men who will become other-Christs. During adolescence — that crucial period of physical, mental and moral development-lasting habits, either good or bad, are generally formed. In a preparatory seminary, perhaps even more than in the major seminary, a boy's mind and character are shaped, and his life receives a direction which often proves decisive.
<p align="justify">
It is for these reasons that the Diocesan priests  congregation operates Saint Joseph Mukasa Seminary. The minor seminary is open to boys of high school age who have completed their grade school education and believe they may be called by God to the priesthood. Of course, only time will tell whether they in fact truly have a vocation from God. But even for a boy who does not have a call to the priesthood, the habits formed during his time in the minor seminary will be of great benefit in his future life in the world.
<p align ="justify">
<b>What is a minor seminary like?</b> The minor seminary is similar to other Catholic boarding schools for boys. Students have a regimented life, which includes daily Mass and prayer, academics and recreation. The studies encompass the regular high school courses in Theology, English, math, science, history, foreign language and physical education. Physical activity is also important, for sports offers an excellent means of character formation by its requirements for teamwork, responsibility and healthful competition. Needless to say, regular confession, spiritual direction and spiritual conferences hold a prominent position in the routine of the seminary. Personal responsibility, cleanliness, order, self-discipline and striving for excellence provide the hallmark of the seminary formation. In short, the entire program of a minor seminary has for its goal the preparation of young men for the major seminary, while it safeguards their vocations.
<p align="justify">
Our minor seminary is named for Saint Joseph Mukasa, who played such a prominent though hidden role in the early life of the great African christianity. May Saint Joseph Mukasa bless our endeavor and send us many worthy boys to become future priests! We ask for your prayers for the success of St. Joseph Mukasa Seminary. If you would like to help us with financial support or have your name added to our mailing list, please use the form on donate tab.

